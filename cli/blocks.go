package main

import (
	"text/template"

	"github.com/spf13/cobra"
)

type block struct {
	Name         string `json:"name"`
	DefaultState int    `json:"defaultState"`
}

func (b block) Ident() string {
	return ident(b.Name)
}

var blockSource = template.Must(template.New("blocks").Parse(
	`// Code generated by gen blocks {{.Version}}; DO NOT EDIT.

package blocks

import gocraft "github.com/lrnxzz/go-craft"

const (
{{- range .Entries}}
	{{.Ident}} gocraft.BlockState = {{.DefaultState}}
{{- end}}
)
`))

func blocksCommand() *cobra.Command {
	return generator[block]{
		name:     "blocks",
		asset:    "blocks.json",
		output:   "blocks.go",
		template: blockSource,
	}.command()
}
