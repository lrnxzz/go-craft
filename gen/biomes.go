package main

import (
	"text/template"

	"github.com/spf13/cobra"
)

type biome struct {
	Name string `json:"name"`
	ID   int    `json:"id"`
}

func (b biome) Ident() string {
	return ident(b.Name)
}

var biomeSource = template.Must(template.New("biomes").Parse(
	`// Code generated by gen biomes {{.Version}}; DO NOT EDIT.

package biomes

import gocraft "github.com/lrnxzz/go-craft"

const (
{{- range .Entries}}
	{{.Ident}} gocraft.BiomeID = {{.ID}}
{{- end}}
)
`))

func biomesCommand() *cobra.Command {
	return generator[biome]{
		name:     "biomes",
		asset:    "biomes.json",
		output:   "biomes.go",
		template: biomeSource,
	}.command()
}
