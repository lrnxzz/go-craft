package main

import (
	"encoding/json"
	"go/format"
	"os"
	"strings"
	"text/template"

	"github.com/spf13/cobra"
)

type biome struct {
	Name string `json:"name"`
	ID   int    `json:"id"`
}

func (b biome) Ident() string {
	return ident(b.Name)
}

var biomeSource = template.Must(template.New("biome").Parse(
	`// Code generated by gen biomes; DO NOT EDIT.

package biome

import gocraft "github.com/lrnxzz/go-craft"

const (
{{- range .}}
	{{.Ident}} gocraft.Biome = {{.ID}}
{{- end}}
)
`))

func biomesCommand() *cobra.Command {
	return &cobra.Command{
		Use:   "biomes",
		Short: "Generate biome/biomes.go from biomes.json",
		Args:  cobra.NoArgs,
		RunE: func(cmd *cobra.Command, args []string) error {
			raw, err := os.ReadFile("biomes.json")
			if err != nil {
				return err
			}

			var biomes []biome
			if err := json.Unmarshal(raw, &biomes); err != nil {
				return err
			}

			var rendered strings.Builder
			if err := biomeSource.Execute(&rendered, biomes); err != nil {
				return err
			}

			formatted, err := format.Source([]byte(rendered.String()))
			if err != nil {
				return err
			}

			if err := os.MkdirAll("biome", 0o755); err != nil {
				return err
			}

			return os.WriteFile("biome/biomes.go", formatted, 0o644)
		},
	}
}
