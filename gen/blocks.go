package main

import (
	"encoding/json"
	"go/format"
	"os"
	"strings"
	"text/template"

	"github.com/spf13/cobra"
)

type block struct {
	Name         string `json:"name"`
	DefaultState int    `json:"defaultState"`
}

func (b block) Ident() string {
	return ident(b.Name)
}

var blockSource = template.Must(template.New("block").Parse(
	`// Code generated by gen blocks; DO NOT EDIT.

package block

import gocraft "github.com/lrnxzz/go-craft"

const (
{{- range .}}
	{{.Ident}} gocraft.BlockState = {{.DefaultState}}
{{- end}}
)
`))

func blocksCommand() *cobra.Command {
	return &cobra.Command{
		Use:   "blocks",
		Short: "Generate block/blocks.go from blocks.json",
		Args:  cobra.NoArgs,
		RunE: func(cmd *cobra.Command, args []string) error {
			raw, err := os.ReadFile("blocks.json")
			if err != nil {
				return err
			}

			var blocks []block
			if err := json.Unmarshal(raw, &blocks); err != nil {
				return err
			}

			var rendered strings.Builder
			if err := blockSource.Execute(&rendered, blocks); err != nil {
				return err
			}

			formatted, err := format.Source([]byte(rendered.String()))
			if err != nil {
				return err
			}

			if err := os.MkdirAll("block", 0o755); err != nil {
				return err
			}

			return os.WriteFile("block/blocks.go", formatted, 0o644)
		},
	}
}
