package main

import (
	"encoding/json"
	"go/format"
	"log"
	"os"
	"strings"
	"text/template"
)

type block struct {
	Name         string `json:"name"`
	DefaultState int    `json:"defaultState"`
}

func (b block) Ident() string {
	var ident strings.Builder
	for _, part := range strings.Split(b.Name, "_") {
		if part != "" {
			ident.WriteString(strings.ToUpper(part[:1]) + part[1:])
		}
	}

	return ident.String()
}

var source = template.Must(template.New("block").Parse(
	`// Code generated by gen; DO NOT EDIT.

package block

import gocraft "github.com/lrnxzz/go-craft"

const (
{{- range .}}
	{{.Ident}} gocraft.BlockState = {{.DefaultState}}
{{- end}}
)
`))

func main() {
	raw, err := os.ReadFile("blocks.json")
	if err != nil {
		log.Fatal(err)
	}

	var blocks []block
	if err := json.Unmarshal(raw, &blocks); err != nil {
		log.Fatal(err)
	}

	var rendered strings.Builder
	if err := source.Execute(&rendered, blocks); err != nil {
		log.Fatal(err)
	}

	formatted, err := format.Source([]byte(rendered.String()))
	if err != nil {
		log.Fatal(err)
	}

	if err := os.MkdirAll("block", 0o755); err != nil {
		log.Fatal(err)
	}
	if err := os.WriteFile("block/blocks.go", formatted, 0o644); err != nil {
		log.Fatal(err)
	}
}
